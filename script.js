        const { jsPDF } = window.jspdf;
        const doc = new jsPDF();

        // Add the Thai font
        doc.addFileToVFS('THSarabunNew.ttf', thSarabunNewFont);
        doc.addFont('THSarabunNew.ttf', 'THSarabunNew', 'normal');
